    public void btnXacNhan(String MasP, String TensP, String gia, String DonViTinh, String XuatXu, Date NN, Date HSD) {
        try {

            Connection connection = getJDBCConnection();

            String sql = "update sanPham  set  Tensp =N'" + TensP + "' , gia=? , Doivitinh=N'" + DonViTinh + "' , XuatXu=N'" + XuatXu + "' ,NgayNhap=?  ,HSD=?,LinkAnh='D:\\SanPham\\" + TensP + ".PNG' where Masp='" + MasP + "'";
            PreparedStatement pst = connection.prepareStatement(sql);
            pst.setString(1, gia);
            pst.setDate(2, new java.sql.Date(NN.getTime()));
            pst.setDate(3, new java.sql.Date(HSD.getTime()));
            pst.executeUpdate();
            DefaultTableModel model = (DefaultTableModel) JtableSanPham.getModel();
            model.setRowCount(0);
            showSanPham();
            JOptionPane.showMessageDialog(null, "Cập nhật thành công");
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

