    public void change() {
        DefaultTableModel model = (DefaultTableModel) JtableSanPham.getModel();
        Object[] row = new Object[7];
        model.setRowCount(0);
        for (int i = 0; i < list.size(); i++) {
            if (list.get(i).getTenSp().toLowerCase().matches("(.*)" + txtTim.getText().toLowerCase() + "(.*)") == true) {
                row[0] = list.get(i).getMaSp();
                row[1] = list.get(i).getTenSp();
                row[2] = list.get(i).getGia();
                row[3] = list.get(i).getDVt();
                row[4] = list.get(i).getXuatS();
                row[5] = list.get(i).getNn();
                row[6] = list.get(i).getHSD();
                model.addRow(row);
            }
        }
    }

    public ArrayList<SanPham> SanPhamList() {
        ArrayList<SanPham> SanPhamList = new ArrayList();

        try {

            Connection connection = getJDBCConnection();

            String sql = "Select * from SanPham";
            // show data
            PreparedStatement statement = connection.prepareCall(sql);
            ResultSet resultSet = statement.executeQuery();
            SanPham p;
            while (resultSet.next()) {

                p = new SanPham(resultSet.getString("MASP"), resultSet.getString("TenSP"), resultSet.getInt("Gia"), resultSet.getString("DoiViTinh"), resultSet.getString("XuatXu"), resultSet.getDate("NgayNhap"), resultSet.getDate("HSD"));
                SanPhamList.add(p);
            }
            // close connection
            connection.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return SanPhamList;
    }

