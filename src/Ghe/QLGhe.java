    public void showGhe() {
        ArrayList<Ghe> list = GheList();
        DefaultTableModel model = (DefaultTableModel) jTableGhe.getModel();
        Object[] row = new Object[7];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getMaG();
            row[1] = list.get(i).getHang();
            row[2] = list.get(i).getCot();
            row[3] = list.get(i).getMaPc();
            row[4] = list.get(i).getGia();
            row[5] = list.get(i).getLoaiG();

            model.addRow(row);
        }

    }

    public void btnXacNhan(String MaGhe, String Hang, String Cot, String MaPC, String Gia, String LoaiG) {
        try {

            Connection connection = ConnectionUtils.getJDBCConnection();
            String sql = "update Ghe  set   Hang='" + Hang + "',Cot='" + Cot + "',MAPC='" + MaPC + "',Gia='" + Gia + "',LoaiGhe='" + LoaiG + "' where MAGhe='" + MaGhe + "'";
            PreparedStatement pst = connection.prepareStatement(sql);
            pst.executeUpdate();
            DefaultTableModel model = (DefaultTableModel) jTableGhe.getModel();
            model.setRowCount(0);
            showGhe();
            JOptionPane.showMessageDialog(null, "Cập nhật thành công");
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

