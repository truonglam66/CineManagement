    public void AddMaPC() {
        ArrayList<String> Item = new ArrayList<>();

        try {

            Connection connection = ConnectionUtils.getJDBCConnection();

            String sql = "Select MaPC from PhongChieu";
            // show data
            PreparedStatement statement = connection.prepareCall(sql);
            ResultSet resultSet = statement.executeQuery();
            String a;

            while (resultSet.next()) {
                a = resultSet.getString("MaPC");
                Item.add(a);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        DefaultComboBoxModel model1 = (DefaultComboBoxModel) CbxMAPC.getModel();
        model1.removeAllElements();
        Object[] MaPC = new Object[Item.size()];
        for (int j = 0; j < Item.size(); j++) {
            MaPC[j] = Item.get(j);
            model1.addElement(MaPC[j]);
        }
        CbxMAPC.setModel(model1);

    }

    public ArrayList<Ghe> GheList() {
        ArrayList<Ghe> GheList = new ArrayList();

        try {

            Connection connection = ConnectionUtils.getJDBCConnection();

            String sql = "Select * from Ghe";
            // show data
            PreparedStatement statement = connection.prepareCall(sql);
            ResultSet resultSet = statement.executeQuery();
            Ghe p;
            while (resultSet.next()) {

                p = new Ghe(resultSet.getString("MAGhe"), resultSet.getString("Hang"), resultSet.getString("Cot"), resultSet.getString("MaPC"), resultSet.getInt("Gia"), resultSet.getString("LoaiGhe"));
                GheList.add(p);
            }
            // close connection
            connection.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return GheList;
    }

